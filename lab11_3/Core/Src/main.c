uint32_t capture, oldcapture;

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim){
	if(htim == &htim2){
		capture = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
		uint32_t T;
		T = capture - oldcapture;
		float fin = (1.0/(T*1.19E-8));
		if(fin > 0.6) freq= (1.0/(T*1.19E-8));
		oldcapture = capture;
		newFreq = 1;
		less1Hz = 0;
	}
}

void HAL_TIM_PeriodElapsedCallback (TIM_HandleTypeDef *htim){
  if (htim == &htim2){
	  if(less1Hz){
		  freq = 0;
		  newFreq = 1;
	  }
	  less1Hz = 1;
  }

}

void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef *htim){
	if (htim == &htim3)
	  {
	      float error = setpoint - freq;
	      float p = KP * error;
	      i = i + KI * DT * error;
	      if(i<-1000) i = -1000;
	      if(i>1000) i = 1000;
	      float d = KD * (error-oldError)/DT;
	      oldError = error;
	      float cval= p + i + d;
	      if(cval >= 1000) cval = 999;
	      if(cval <= 0) cval = 1;
	      __HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,cval*42);

	  }
}


